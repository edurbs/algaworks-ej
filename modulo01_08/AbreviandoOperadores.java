package modulo01_08;

public class AbreviandoOperadores {
	public static void main (String[] args){
		int idadeA = 10;
		idadeA += 10;
		System.out.println(idadeA);
		
		int idadeB = 30;
		idadeB -= 10;
		System.out.println(idadeB);
		
		int idadeC = 10;
		idadeC *= 8;
		System.out.println(idadeC);
		
		int idadeD = 40;
		idadeD /= 2;
		System.out.println(idadeD);

		int idadeF = 15;
		idadeF %= 4;
		System.out.println(idadeF);
	}
}